CREATE DEFINER=`root`@`localhost` PROCEDURE `pDoubleToDegMinSec`(IN Dub double, OUT Deg smallint, OUT Min smallint, OUT Sec smallint)
BEGIN
	DECLARE DegTemp, MinTemp, SecTemp SMALLINT;
	DECLARE AbsDub, MinFrac, MinWhole, SecFrac, SecWhole  DOUBLE;

	
	SET AbsDub = ABS(Dub);

	
	SET DegTemp = FLOOR(AbsDub);

	
	SET MinFrac = AbsDub - DegTemp;

	
	SET MinWhole = MinFrac * 60;

	
	SET MinTemp = FLOOR(MinWhole);

	
	SET SecFrac = MinWhole - MinTemp;

	
	SET SecTemp = ROUND(SecFrac * 60);

	
	IF SecTemp = 60 THEN 
		SET SecTemp = 0;
		SET MinTemp = MinTemp + 1;
	END IF;
	IF MinTemp = 60 THEN
		SET MinTemp = 0;
		SET DegTemp = DegTemp + 1;
	END IF;
	
	
	IF Dub < 0 THEN
		SET DegTemp = DegTemp * (-1);
	END IF; 
	
	
	SET Deg = DegTemp;
	SET Min = MinTemp;
	SET Sec = SecTemp;
END ;;
