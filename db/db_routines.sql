CREATE DEFINER=`root`@`localhost` FUNCTION `get_visit_days_in_period`(VisitBeg datetime, VisitEnd datetime, PerBeg date, PerEnd date, ActualCount float, ActualDays float) RETURNS int
    DETERMINISTIC
    SQL SECURITY INVOKER
BEGIN
	DECLARE actualPeriodBeg, actualPeriodEnd, visitBegInZone, visitEndInZone DATETIME;
	DECLARE entireVisitPeriod, actualPeriod INT;
	SET visitBegInZone = CONVERT_TZ(VisitBeg,'UTC','US/Pacific');
	SET visitEndInZone = CONVERT_TZ(VisitEnd,'UTC','US/Pacific');
	SET actualPeriodBeg = fSetFirstCountDate(visitBegInZone, CONCAT(PerBeg, ' 00:00:00'));
	SET actualPeriodEnd = fSetLastCountDate(visitEndInZone, CONCAT(PerEnd, ' 23:59:59'));
	SET actualPeriod = DATEDIFF(actualPeriodEnd, actualPeriodBeg) + 1;
	SET entireVisitPeriod = DATEDIFF(
		CONVERT_TZ(VisitEnd,'UTC','US/Pacific'), 
		CONVERT_TZ(VisitBeg,'UTC','US/Pacific')
	) + 1;
	
	RETURN IF (
		ActualDays > 0,
		ABS(
			CEILING(
				ActualCount * ActualDays * actualPeriod / entireVisitPeriod
			)
		),
		ABS(
			CEILING(
				ActualCount * actualPeriod 
			)
		)
	);
END ;;
